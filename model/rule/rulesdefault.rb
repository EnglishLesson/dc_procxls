class RulesDefault
  def getCode
    @code
  end

  def setCode(code)
    @code = code
  end

  def getCodeRule
    @codeRule
  end

  def setCodeRule(codeRule)
    @codeRule = codeRule
  end

  def getCodeException
    @codeException
  end

  def setCodeException(codeException)
    @codeException = codeException
  end
end
