class RulesDefault
  def getCode
    @code
  end

  def setCode(code)
    @code = code
  end

  def getRuleId
    @ruleId
  end

  def setRuleId(ruleId)
    @ruleId = ruleId
  end

  def getExceptionId
    @exceptionId
  end

  def setExceptionId(exceptionId)
    @exceptionId = exceptionId
  end
end
